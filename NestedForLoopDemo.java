class NestedForLoopDemo{
public static void main(String args[]){
for(int i=1;i<=3;i++)
System.out.print(i+" ");
System.out.println();
{
for(int j=4;j<=6;j++)
{ System.out.print(j+" ");
}
System.out.println();
}
for(int g=7;g<=9;g++)
{ System.out.print(g+" ");
}
System.out.println();
}
}
 