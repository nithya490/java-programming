class Increment{
public static void main (String args[]){
int a = 10;
int b = ++a;
int c = b++;
System.out.println(c);
System.out.println(c++);
System.out.println(++b);
System.out.println(a++);
System.out.println(++c);
System.out.println(b++);
System.out.println(++a);
System.out.println(a);
System.out.println(b);
System.out.println(c);
}
}












